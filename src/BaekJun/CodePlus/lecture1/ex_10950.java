package BaekJun.CodePlus.lecture1;

import java.util.Scanner;

public class ex_10950 {
    public static void main(String arg[]) {
        Scanner sc = new Scanner(System.in);
        int a,b;

        while (sc.hasNextInt()) {
                a = sc.nextInt();
                b = sc.nextInt();
                System.out.println(a + b);
        }
    }
}
